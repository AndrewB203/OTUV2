k0 = 1; 
T0 = 0.1; 
T1 = 0.7; 
k1 = 1.6; 

% Передаточные функции компонентов разомкнутой системы
W2 = tf(k1, [T1, 1]); 
W3 = tf(1, [T0, 1]); 
W4 = tf(1, [1, 0]); 

% Передаточная функция разомкнутой системы
W = W2 * W3 * W4;

% Передаточная функция замкнутой системы (обратная связь с единичным усилением)
F = feedback(W, 1);

% Шаг дискретизации
T = 0.5;  % например, 10 мс. Подберите под вашу задачу

% Аппроксимация Тастина (билинейное преобразование)
F_d = c2d(F, T, 'tustin');

% Получение числителя и знаменателя
[num, den] = tfdata(F_d, 'v');

% Вывод массивов числителя и знаменателя
disp('Числитель дискретной передаточной функции:');
disp(num);

disp('Знаменатель дискретной передаточной функции:');
disp(den);
